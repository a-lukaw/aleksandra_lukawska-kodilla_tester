package com.kodilla.basic_assertion;

public class Calculator {
    int a;
    int b;
    int c;

    public Calculator() {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int sum(int a, int b) {
        return a + b;
    }
    public int subtract(int a, int b) {
        return a - b;
    }

    public int square(int c) {
        return (int) Math.pow(c, 2);
    }
}